def convert_excel_float_to_str(string,pianyi=0):
    if pianyi==0:
        value=string
    else:
        if '.' in string:
            #先补齐0
            mylist=string.split('.')
            new_string=string
            if pianyi>0:
                if len(mylist[1])-pianyi<1:
                    hou=mylist[1]+'0'*(pianyi+1-len(mylist[1]))
                    qian='' if mylist[0]=='0'*len(mylist[0]) else mylist[0]

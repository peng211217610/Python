def qudiao_qianmian_deling(value):
    #偏移之后，出现01.0这类时去掉前面的0
    if value.split('.')[0][0]=='0':
        value_qian,value_hou=value.split('.')
        for m in range(len(value_qian)):
            if value_qian[0]=='0':
                value_qian=value_qian[1:]
        value=value_qian+'.'+value_hou
    return value

def qudiao_houmian_deling(value):
    #偏移之后出现0.10这类时去掉后面的0
    if value.split('.')[1][-1]=='0':
        value_qian,value_hou=value.split('.')
        for m in range(len(value_hou)):
            if value_hou[-1]=='0':
                value_hou=value_hou[:-1]
            if value_hou=='':
                value_hou='0'
        value=value_qian+'.'+value_hou
    return value


